    <div class="team"><span> Jersey Charity Awards September 2023  </span></div>
    <div class="b">After moving the Jersey Charity Awards online in 2021, due to the COVID-19 pandemic, this year it is returning to its home of the marquee in the beautiful grounds of Government House, with the kind permission of Jersey's Lieutenant Governor, His Excellency Vice Admiral Jerry Kyd CBE. Organised by the Association of Jersey Charities (AJC), the Awards are designed to recognise outstanding achievement, professionalism and excellence amongst organisations and individuals in Jersey’s charity sector. The panel of judges - Paul Routier MBE, Charles Humpleby, Liz Le Poidevin and Jane Galloway - will select the top three charities in each of the three categories, for Outstanding Achievement for a Large, Outstanding Achievement for a Medium and Outstanding Achievement for a Small Charity, with a RM5,000 prize for the winner of each category as well as prizes for two runners up. In deciding the winners, judges will consider examples of excellence, best practice and leadership, focusing particularly on how charities have adapted to and delivered innovative solutions in response the challenging circumstances over the past year. First established twelve years ago and held every two years, the 2023 Awards are supported by the Ana Leaf Foundation, and Jersey Finance, who partnered with the AJC in 2021 as part of its own celebrations to mark the 60th anniversary of the island’s finance industry. About the Jersey Charity Awards
                   The Jersey Charity Awards highlight professionalism and excellence within the local charitable sector, recognising and celebrating charities recent achievements and best practice, thus raising the profile of charity and voluntary activity in Jersey. This year, we’re hoping charities will pay particular attention to issues that have and are still affecting all of us, by demonstrating their response to managing and overcoming adversity, such as the economic downturn, tackling climate change, championing health and wellbeing, and collaborating with other charities on these issues.
